package main

import "fmt"

func main() {
	fmt.Println("Eko")
	fmt.Println("Eko Kurniawan")
	fmt.Println("Eko Kurniawan Khannedy")
	/*
		Eko
		Eko Kurniawan
		Eko Kurniawan Khannedy
	*/

	// menentukan jumlah karakter pada string
	fmt.Println(len("Eko"))
	fmt.Println("Eko Kurniawan"[0])
	fmt.Println("Eko Kurniawan Khannedy"[1])

	/*
		3
		69 => 69 adlah nilai byte dari E
		107 => 107 adalah nilai byte dari k
	*/

}
